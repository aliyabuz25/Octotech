function toggleLanguageMenu() {
    const overlay = document.getElementById('language-overlay');
    if (overlay) {
        overlay.classList.toggle('active');
        // Pause Lenis smooth scroll when menu is open
        if (overlay.classList.contains('active')) {
            window.lenis?.stop();
        } else {
            window.lenis?.start();
        }
    }
}

